package Class7;

public class ForLoops {
    public static void main(String[] args) {


        int number = 0;
        while (number < 10) {
            System.out.println(number);
            number++;
        }
        for (int i = 0; number < 10; number++) {
            {
                System.out.println(number);


            }
        }
    }}






