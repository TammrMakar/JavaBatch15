package PracticeAtHome;

public class Practice11switch {
    public static void main(String[] args) {

       String day="Monday";
       switch (day){
           case "Monday":
               System.out.println("My favorite day");
               break;
           case "tuesday":
               System.out.println("Is not My day");
               break;
           case "friday":
               System.out.println("My Birthday");
               break;
           default:
               System.out.println("No Day");
       }
        }
    }



